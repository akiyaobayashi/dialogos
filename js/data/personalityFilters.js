export function buildPersonalityFilter(sage) {
  const filter = PERSONALITY_FILTERS[sage.id];
  if (!filter) return "";

  return `
【人格フィルタ】
核: ${filter.core}

【返答前に必ず行うこと】
${filter.findFirst}

見るべき層: ${filter.lens}
会話のリズム: ${filter.rhythm}
語彙・象徴: ${filter.diction}

必ず行うこと:
${filter.mustDo.map((item) => `- ${item}`).join("\n")}

避けること:
${filter.avoid.map((item) => `- ${item}`).join("\n")}
`;
}
